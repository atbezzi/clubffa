			El Club de las Fuerzas Armadas Córdoba fue fundado el 24 de Diciembre del año 1965 según consta en su primer Acta.<br>
			Sus comienzos como institución civil sin fines de lucro, social, cultural y deportiva, fueron difíciles y constituyo para los socios fundadores un verdadero desafío.<br>
			De acuerdo a lo trascripto en el Acta Nº 1, el día 24 de Diciembre de 1965, fueron convocados al efecto los camaradas simpatizantes con la idea de la creación en la ciudad de Córdoba de una entidad social que agrupe al Personal Superior de las Fuerzas Armadas (Ejército, Marina y Aeronáutica) y sus pensionistas, se reunieron en el salón de actos del Jockey Club Córdoba que fue cedido a tal fin y se dio comienzo a esta convocatorio escuchando la palabra del señor General de División (R) D. Aristides Renato Ruibal, quien manifestó y resalto sobre la necesidad de concretar esta iniciativa. Al ser consultados los presentes por aclamación, decidieron apoyas lo propuesto.<br>
			En tal sentido se aprobó que la Institución se denominaría Club Militar de las Fuerzas Armadas Córdoba y con cede en la ciudad de Córdoba.<br>
			Acto seguido se convoco para el día 7 de Enero de 1966 a los presentes en su primera Asamblea, con el objetivo de constituir una Comisión Directiva Provisoria que se abarcaría a confeccionar un Proyecto de Estatutos y a la difusión de la Constitución del Club y de la conscripción de Socios correspondientes.<br>
